/// Escape a field value for pipe-separated MD:
///   literal `|` → `\|` (so parser can tell it apart from field separator)
///   literal `\n` → `\\n` (preserve newlines in single-line field)
/// Order matters: pipe first, then newline, so `|` in description with newlines works.
fn escape_field(s: &str) -> String {
    s.replace('|', "\\|").replace('\n', "\\n")
}

/// Reverse `escape_field` on parse: unescape `\n` then `\|`.
fn unescape_field(s: &str) -> String {
    s.replace("\\n", "\n").replace("\\|", "|")
}

/// F-026 v2 format — 8 pipe-separated fields:
/// `id | date | description | severity | category | status | fix_attempts | comment`
/// Generated by the app. Confirmed bugs are excluded by caller.
pub fn generate_bug_reports(bugs: &[FileBugNote]) -> String {
    let mut md = String::new();
    md.push_str("# Bug reports\n\n");
    md.push_str("## Bug workflow\n\n");
    md.push_str("1. **User** creates a bug in the app → status `created`\n");
    md.push_str("2. **Developer/LLM** takes it in progress → status `in-progress`\n");
    md.push_str(
        "3. **Developer/LLM** fixes it and writes a comment on what was done → status `testing` (this transition bumps `fix_attempts` +1)\n",
    );
    md.push_str("4. **User** verifies: ok → status `confirmed` (LLM removes the row on next cleanup pass) / not ok → status `rejected` + comment explaining why\n\n");
    md.push_str("**LLM may edit only `status` and `comment`.** Other fields (description/severity/category) are user-owned; `id`/`date` immutable; `fix_attempts` app-managed.\n\n");
    md.push_str("**LLM must not create new bug rows** — use the app's \"+ Add bug\" button so id/date/DB-stats are wired. To flag a finding, add a `T-NNN` to `todo.md` instead.\n\n");
    md.push_str("**Escape:** a literal `|` inside description/comment is written as `\\|`. A literal newline is `\\n`.\n\n");
    md.push_str("## Open bugs\n\n");
    for bug in bugs {
        let description = escape_field(&bug.description);
        let comment = escape_field(bug.comment.as_deref().unwrap_or(""));
        md.push_str(&format!(
            "- {} | {} | {} | {} | {} | {} | {} | {}\n",
            bug.id,
            bug.date,
            description,
            bug.severity,
            bug.category,
            bug.status,
            bug.fix_attempts,
            comment,
        ));
    }
    md
}

/// Parse bug-reports.md v2 (8 pipe-separated fields). Legacy v1 (10 fields with screen +
/// reproduction) removed in v0.13.9 after user-side migration — all files were re-saved
/// via BugNotes "⬇ v2" button. See `parse_markdown_legacy` for the much older "# Bug List:"
/// pre-pipe format (import-only, unrelated).
pub fn parse_bug_reports(content: &str) -> (Vec<FileBugNote>, Vec<String>) {
    let mut bugs = Vec::new();
    let mut warnings = Vec::new();

    for line in content.lines() {
        let trimmed = line.trim();
        let rest_opt = trimmed.strip_prefix("- [ ] ").or_else(|| {
            trimmed
                .strip_prefix("- ")
                .filter(|r| r.starts_with("B-") || r.starts_with("VB-"))
        });
        let Some(rest) = rest_opt else { continue };
        let parts: Vec<String> = split_pipe_respecting_escape(rest)
            .into_iter()
            .map(|s| s.trim().to_string())
            .collect();

        if parts.len() != 8 {
            warnings.push(format!(
                "Unparseable bug line ({} fields, expected 8): {}",
                parts.len(),
                line
            ));
            continue;
        }
        bugs.push(FileBugNote {
            id: parts[0].clone(),
            date: parts[1].clone(),
            description: unescape_field(&parts[2]),
            severity: parts[3].clone(),
            category: parts[4].clone(),
            status: parts[5].clone(),
            fix_attempts: parts[6].parse::<i32>().unwrap_or(0),
            comment: if parts[7].is_empty() {
                None
            } else {
                Some(unescape_field(&parts[7]))
            },
        });
    }

    (bugs, warnings)
}

