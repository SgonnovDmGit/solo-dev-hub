#[derive(Debug, Clone)]
pub struct ParsedHeader {
    pub github_name: String,
}

#[derive(Debug, Clone)]
pub struct ParsedBug {
    #[allow(dead_code)]
    pub id: Option<String>,
    pub title: String,
    pub description: Option<String>,
    pub priority: String,
    pub category: String,
    pub is_resolved: bool,
    pub fix_attempts: i32,
    #[allow(dead_code)]
    pub created_at: Option<String>,
    #[allow(dead_code)]
    pub resolved_at: Option<String>,
}

#[derive(Debug)]
pub struct ParsedMarkdown {
    #[allow(dead_code)]
    pub github_name: String,
    pub bugs: Vec<ParsedBug>,
    pub skipped_lines: usize,
}

pub fn parse_header(md: &str) -> Option<ParsedHeader> {
    for line in md.lines() {
        let trimmed = line.trim();
        if let Some(rest) = trimmed.strip_prefix("# Bug List:") {
            let github_name = rest.trim().to_string();
            if !github_name.is_empty() {
                return Some(ParsedHeader { github_name });
            }
        }
    }
    None
}

pub fn parse_bug_entry(line: &str, desc_lines: &[&str]) -> Option<ParsedBug> {
    let trimmed = line.trim();

    let (is_resolved, rest) = if let Some(r) = trimmed.strip_prefix("- [ ]") {
        (false, r.trim())
    } else if let Some(r) = trimmed.strip_prefix("- [x]") {
        (true, r.trim())
    } else {
        return None;
    };

    let (priority, category, rest) = if let Some(r) = rest.strip_prefix("**[") {
        if let Some(end) = r.find("]**") {
            let inner = &r[..end];
            let after = r[end + 3..].trim();
            if let Some(pipe_pos) = inner.find('|') {
                let priority = inner[..pipe_pos].to_lowercase();
                let category = inner[pipe_pos + 1..].to_lowercase();
                (priority, category, after)
            } else {
                let priority = inner.to_lowercase();
                (priority, "unknown".to_string(), after)
            }
        } else {
            ("medium".to_string(), "unknown".to_string(), rest)
        }
    } else {
        ("medium".to_string(), "unknown".to_string(), rest)
    };

    let id = if let Some(comment_start) = rest.find("<!-- id:") {
        let after = &rest[comment_start + 8..];
        if let Some(comment_end) = after.find(" -->") {
            let raw = after[..comment_end].trim().to_string();
            if raw.is_empty() {
                None
            } else {
                Some(raw)
            }
        } else {
            None
        }
    } else {
        None
    };

    let fix_attempts = if let Some(att_start) = rest.find("[attempts:") {
        let after = &rest[att_start + 10..];
        let trimmed_after = after.trim_start();
        if let Some(att_end) = trimmed_after.find(']') {
            trimmed_after[..att_end].trim().parse::<i32>().unwrap_or(0)
        } else {
            0
        }
    } else {
        0
    };

    let title_end = rest
        .find(" [attempts:")
        .or_else(|| rest.find(" <!-- id:"))
        .unwrap_or(rest.len());
    let title = rest[..title_end].trim().to_string();

    if title.is_empty() {
        return None;
    }

    let mut desc_parts: Vec<String> = Vec::new();
    let mut created_at: Option<String> = None;
    let mut resolved_at: Option<String> = None;

    for desc_line in desc_lines {
        let dl = desc_line.trim();
        if let Some(content) = dl.strip_prefix("> ") {
            if let Some(date) = content.strip_prefix("Created: ") {
                created_at = Some(date.trim().to_string());
            } else if let Some(date) = content.strip_prefix("Resolved: ") {
                resolved_at = Some(date.trim().to_string());
            } else {
                desc_parts.push(content.to_string());
            }
        } else if dl == ">" {
            desc_parts.push(String::new());
        }
    }

    let description = if desc_parts.is_empty() {
        None
    } else {
        Some(desc_parts.join("\n"))
    };

    Some(ParsedBug {
        id,
        title,
        description,
        priority,
        category,
        is_resolved,
        fix_attempts,
        created_at,
        resolved_at,
    })
}

/// Parse legacy "# Bug List:" format markdown.
pub fn parse_markdown_legacy(md: &str) -> Option<ParsedMarkdown> {
    let header = parse_header(md)?;
    let lines: Vec<&str> = md.lines().collect();

    let mut bugs: Vec<ParsedBug> = Vec::new();
    let mut skipped_lines = 0usize;
    let mut i = 0;

    while i < lines.len() {
        let line = lines[i];
        let trimmed = line.trim();

        if trimmed.starts_with("- [ ]") || trimmed.starts_with("- [x]") {
            let mut desc_lines: Vec<&str> = Vec::new();
            let mut j = i + 1;
            while j < lines.len() {
                let next = lines[j];
                let next_trimmed = next.trim();
                if next_trimmed.starts_with('>')
                    || (next.starts_with("  ") && !next_trimmed.starts_with("- "))
                {
                    desc_lines.push(next);
                    j += 1;
                } else {
                    break;
                }
            }

            if let Some(bug) = parse_bug_entry(line, &desc_lines) {
                bugs.push(bug);
            } else {
                skipped_lines += 1;
            }

            i = j;
        } else {
            i += 1;
        }
    }

    Some(ParsedMarkdown {
        github_name: header.github_name,
        bugs,
        skipped_lines,
    })
}

// ── F-021 TODO / DONE parsers ────────────────────────────────────────────────

/// Detect YYYY-MM-DD, DD.MM.YYYY or DD/MM/YYYY at trimmed-equal-10 length.
/// Used as an anchor in the done parser so column shift (no id, or extra middle field)
/// doesn't land date-text in the description column.
fn is_date_like(s: &str) -> bool {
    let s = s.trim();
    if s.len() != 10 {
        return false;
    }
    let b = s.as_bytes();
    // YYYY-MM-DD
    if b[4] == b'-' && b[7] == b'-' {
        return b
            .iter()
            .enumerate()
            .all(|(i, c)| matches!(i, 4 | 7) || c.is_ascii_digit());
    }
    // DD.MM.YYYY or DD/MM/YYYY
    if (b[2] == b'.' || b[2] == b'/') && b[5] == b[2] {
        return b
            .iter()
            .enumerate()
            .all(|(i, c)| matches!(i, 2 | 5) || c.is_ascii_digit());
    }
    false
}

/// True if the line's first stripped word could be a task ID we want to accept
/// (`T-…`, `F-…`, `B-…`, `v0…`, or begins with an ASCII digit for generic IDs).
fn looks_like_task_id_start(r: &str) -> bool {
    r.chars().next().map_or(false, |c| {
        c == 'T' || c == 'F' || c == 'B' || c == 'v' || c.is_ascii_digit()
    })
}

/// Parse `docs/todo.md` tasks. Canonical 5 fields: id | description | effort | priority | status.
/// Tolerant: accepts 3–6+ fields. `status` is always the last field; `id` is always the first.
/// With 4 fields: id | description | effort | status (priority empty).
/// With 3 fields: id | description | status (effort and priority empty).
/// With 6+ fields: description = middle slots joined by ` | ` (e.g. when a feature-id prefix
/// widens the line like `T-037 | F-019 | desc | L | must | open`).
pub fn parse_todo_tasks(content: &str) -> (Vec<TodoTask>, Vec<String>) {
    let mut tasks = Vec::new();
    let mut warnings = Vec::new();
    for line in content.lines() {
        let trimmed = line.trim();
        let rest_opt = trimmed
            .strip_prefix("- [ ] ")
            .or_else(|| trimmed.strip_prefix("- ").filter(|r| looks_like_task_id_start(r)));
        let Some(rest) = rest_opt else { continue };
        let parts: Vec<String> = split_pipe_respecting_escape(rest)
            .into_iter()
            .map(|s| s.trim().to_string())
            .collect();
        let n = parts.len();
        if n < 3 {
            warnings.push(format!("Unparseable todo line: {}", line));
            continue;
        }
        let id = parts[0].clone();
        let (description, effort, priority, status, created_at) = match n {
            3 => (
                parts[1].clone().replace("\\n", "\n"),
                String::new(),
                String::new(),
                parts[2].clone(),
                String::new(),
            ),
            4 => (
                parts[1].clone().replace("\\n", "\n"),
                parts[2].clone(),
                String::new(),
                parts[3].clone(),
                String::new(),
            ),
            5 => (
                parts[1].clone().replace("\\n", "\n"),
                parts[2].clone(),
                parts[3].clone(),
                parts[4].clone(),
                String::new(),
            ),
            6 => (
                parts[1].clone().replace("\\n", "\n"),
                parts[2].clone(),
                parts[3].clone(),
                parts[4].clone(),
                parts[5].clone(),
            ),
            _ => {
                // n >= 7: last 4 = effort, priority, status, created_at; middle = description
                let desc_slots = &parts[1..n - 4];
                (
                    desc_slots.join(" | ").replace("\\n", "\n"),
                    parts[n - 4].clone(),
                    parts[n - 3].clone(),
                    parts[n - 2].clone(),
                    parts[n - 1].clone(),
                )
            }
        };
        tasks.push(TodoTask {
            id,
            description,
            effort,
            priority,
            status,
            created_at,
        });
    }
    (tasks, warnings)
}

/// Parse `docs/done.md` (v0.13.9+ format).
///
/// Structure:
/// ```
/// ## 2026-04-21             ← date header; applies to tasks below until next header
/// - D-001 | Task X | v0.13.9
/// -  | Task without id | v0.13.9
/// ```
///
/// Line format (3 pipe-separated fields): `- <id?> | <description> | <version>`.
/// - If `id` is empty → assign `D-NNN` from a per-file counter (in-memory only — file is NOT modified)
/// - Tolerant: accepts 2 fields (description + version) or 3 fields (with id)
/// - `date` is inherited from the nearest preceding `## <date>` section header. If none, empty.
/// - `## <anything>` is scanned for a date-like substring (YYYY-MM-DD / DD.MM.YYYY / DD/MM/YYYY),
///   so legacy headers like `## День 29.03.2026` still produce a date.
///
/// Legacy `- [x] ...` rows (checkbox, 4 fields with `commit`) are NOT supported — user rewrites
/// per-project when migrating. Parser tolerates them by extracting what it can; unparseable lines
/// produce warnings.
pub fn parse_done_tasks(content: &str) -> (Vec<DoneTask>, Vec<String>) {
    let mut tasks = Vec::new();
    let mut warnings = Vec::new();
    let mut current_date = String::new();
    let mut auto_counter: i32 = 0;

    for line in content.lines() {
        let trimmed = line.trim();

        // Section header with date: `## <...date...>`.
        if let Some(rest) = trimmed.strip_prefix("## ") {
            if let Some(d) = rest.split_whitespace().find(|w| is_date_like(w)) {
                current_date = d.trim().to_string();
            }
            continue;
        }

        // Task line: `- ...`. Accept legacy `- [x] ` / `- [X] ` prefixes for transition.
        let rest_opt = trimmed
            .strip_prefix("- [x] ")
            .or_else(|| trimmed.strip_prefix("- [X] "))
            .or_else(|| trimmed.strip_prefix("- "));
        let Some(rest) = rest_opt else { continue };

        let parts: Vec<String> = split_pipe_respecting_escape(rest)
            .into_iter()
            .map(|s| s.trim().to_string())
            .collect();

        let (id_raw, description, version) = match parts.len() {
            // Canonical v0.13.9+ — 3 fields (id slot may be empty but pipes present).
            3 => (parts[0].clone(), parts[1].clone(), parts[2].clone()),
            // Legacy migration tolerance: old `- [x] id | desc | date | commit` (4 fields).
            // Keep id/desc, drop inline date (section-header wins), treat commit as version slot.
            4 => (parts[0].clone(), parts[1].clone(), parts[3].clone()),
            _ => {
                warnings.push(format!(
                    "Unparseable done line ({} fields, expected 3 — id slot may be empty but pipes must be present): {}",
                    parts.len(),
                    line
                ));
                continue;
            }
        };

        let id = if id_raw.is_empty() {
            auto_counter += 1;
            format!("D-{:06}", auto_counter)
        } else {
            id_raw
        };

        tasks.push(DoneTask {
            id,
            description: description.replace("\\n", "\n"),
            date: current_date.clone(),
            version,
        });
    }

    (tasks, warnings)
}

/// Parse docs/done.md and count entries per `## YYYY-MM-DD` section header
/// falling within [start, end] (inclusive). Returns Vec<(date, count)> sorted by date.
/// Missing file → empty vec (not an error).
pub fn parse_done_entries_in_period(
    path: &std::path::Path,
    start: &str,
    end: &str,
) -> std::io::Result<Vec<(String, i64)>> {
    if !path.exists() {
        return Ok(vec![]);
    }
    let content = std::fs::read_to_string(path)?;
    let mut current_date: Option<String> = None;
    let mut counts: std::collections::HashMap<String, i64> = std::collections::HashMap::new();

    for line in content.lines() {
        let trimmed = line.trim();
        if let Some(date_str) = trimmed.strip_prefix("## ") {
            let date = date_str.trim();
            if date.len() == 10
                && chrono::NaiveDate::parse_from_str(date, "%Y-%m-%d").is_ok()
            {
                current_date = Some(date.to_string());
            } else {
                current_date = None;
            }
        } else if trimmed.starts_with("- ") {
            if let Some(d) = &current_date {
                if d.as_str() >= start && d.as_str() <= end {
                    *counts.entry(d.clone()).or_insert(0) += 1;
                }
            }
        }
    }

    let mut out: Vec<(String, i64)> = counts.into_iter().collect();
    out.sort_by(|a, b| a.0.cmp(&b.0));
    Ok(out)
}

